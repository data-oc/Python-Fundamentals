"""
Python is dynamically typed.

1. Numeric
    1.1 Integer
    1.2 Floating point number
    1.3 Complex number
"""

# 1.1 Integer
integer_1 = 11
integer_2 = -22
number_base_2 = bin(20)
number_base_8 = oct(20)
number_base_16 = hex(20)

print("integer_1 =", integer_1)
print("integer_2 =", integer_2)
print("number_base_2 =", number_base_2)
print("number_base_8 =", number_base_8)
print("number_base_16 =", number_base_16)


# 1.2 Floating point number
grand_total = 1234.56789
print("grand_total =", grand_total)


# 1.3 Complex number
complex_number = -4.5 + 2j
print("complex_number =", complex_number)

